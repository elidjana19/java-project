public abstract class Shoferi {
    String emri, skuadra;

    public String getEmri() {
        return emri;
    }

    public void setEmri(String emri) {
        this.emri = emri;
    }

    public String getSkuadra() {
        return skuadra;
    }

    public void setSkuadra(String skuadra) {
        this.skuadra = skuadra;
    }
}
